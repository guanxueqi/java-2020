package Array;

public class Demo02Array {
    public static void main(String[] args) {
        //静态省略格式
        int [] arrayA = {1,2,3};

        //静态初始化标准格式可以拆分为两个步骤
        int[] ArrayB;
        ArrayB = new int[]{1,2,3,4,5};
        //动态初始化也可以拆分为两个步骤
        int [] arrayC;
        arrayC = new int[20];

    }
}
